package April2024.Class18;

public class whileDemo {
    public static void main(String[] args) {
        //While sample************************************************************
        int inputNumber =1;

        while (inputNumber<=10) {
            System.out.println(inputNumber);
            inputNumber++;
        }
        //************************************************************

        //For sample************************************************************
        for (int inputNumber1=1; inputNumber1 <=10; inputNumber1++) {
            System.out.println(inputNumber1);
            
        }
        //************************************************************

        /*
         When to use for or while
         
         when know how many loop times / loop range use for
         When you only know the ending condition use while
         
         */
    }
}
