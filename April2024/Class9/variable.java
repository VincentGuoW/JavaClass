package April2024.Class9;

public class variable {
    
    public static void main(String[] args) {
        int a=1, b=2, c=3;
        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
        /* set three variable at a time */

        System.out.println(11);    //   number => Decimal  ==>        11
        System.out.println(011);   //0 +number => Octal    ==>  8+1=  9
        System.out.println(0b11);  //ob+number => Binary   ==>  2+1=  3
    }

}
